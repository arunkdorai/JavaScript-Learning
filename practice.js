// Arithmetic Operator

// Addition
console.log(10 + 20);

// Subtraction
console.log(20 - 5)

// Multiplication
console.log(20 * 2)

// Division
console.log(20 / 2) // Quotient ==> 10

// Modulus
console.log(20 % 3) // Remainder ==> 2

// Exponential
console.log(3 ** 3)

// Increment - Decrement

let num = 10;

num = num + 1

console.log(num)

// Increment (++)

let num1 = 15

num1++ // Post Increment ==> num1 = num1 +1 = 15 + 1 = 16

++num1 // Pre Increment ==> num1 = num1 + 1 = 16 + 1 = 17

console.log(num1)

// Decrement (--)

let num2 = 20

num2-- // Post Decrement ==> num2 = num2 - 1 = 20 - 1 = 19

--num2 // Pre Decrement ==> num2 - 1 = 19 - 1 = 18

console.log(num2)